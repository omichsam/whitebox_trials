                    <div>
                        <strong style="font-weight: 700;">1. Do I need to have intellectual property
                            protection?</strong><br>
                        Yes. It is recommended that you apply for the protection of your
                        intellectual property before you submit the same to Whitebox. It is
                        important to safeguard your intellectual property rights to secure your
                        creativity and inventiveness and prevent the information contained in
                        your submission from being appropriated by a third party.
                    </div>
                    &nbsp;
                    <div>
                        <strong style="font-weight: 700;">
                            2. When should I protect my intellectual property?
                        </strong>
                        <br>
                        You should take steps to protect your intellectual property at earliest
                        possible juncture. It is recommended that you should not disclose any
                        information regarding your intellectual property if you have not
                        registered it or applied for registration.
                        Disclosing such information before registration may lead to the
                        appropriation of the information by a third party and hinder your
                        ability to subsequently file for registration.
                    </div>
                    &nbsp;
                    <div>
                        <strong style="font-weight: 700;">
                            3. What forms of protection are available for my intellectual property?
                        </strong>
                        <br>
                        Intellectual property may be protected using Patents, Trademarks and
                        Copyrights. You can protect your Intellectual property by registering
                        with the relevant government agency. Ownership can be by a natural
                        person or registered legal entity.
                    </div>
                    &nbsp;
                    <div>
                        <strong style="font-weight: 700;">
                            4. What are the benefits of protecting my intellectual property?
                        </strong>
                        <br>
                        Intellectual property is a business asset and therefore securing it is
                        essential to your business and brand. Secured intellectual property
                        rights exclude third parties from appropriating your rights thus adding
                        value to your business and giving you a competitive edge.
                    </div>
                    &nbsp;

                    <div>

                        <strong style="font-weight: 700;">Patents</strong> afford protection to
                        inventions in the technological or industrial sector. The Kenya
                        Industrial Property Institute undertakes the registration of patents.
                        The requirement for the registration of a patent can be found <a
                            href="https://www.kipi.go.ke/index.php/patents" target="_blank">here</a>. <br>
                    </div>
                    &nbsp;
                    <div>
                        <strong style="font-weight: 700;">Copyright </strong> affords protection
                        to artistic works of musical, audio visual, literary and artistic
                        nature. To acquire copyright for a particular work, the Copyright Act
                        does not require registration. It is however recommended that you
                        register in order to claim exclusive rights to the works. Registration
                        of copyright is done by the Kenya Copyright Board. The process of
                        registering copyright can be found <a
                            href="https://www.copyright.go.ke/8-program/2-copyright-registration.html"
                            target="_blank">here</a> .<br>
                    </div>
                    &nbsp;
                    <div>
                        <strong style="font-weight: 700;"> Trademarks </strong> afford
                        protection to brand names, marks, signs and logos associated with
                        certain goods and services offered by an enterprise. Use of the
                        trademark serves to distinguish the goods and services of such
                        enterprise. The process of registering trademarks is undertaken by the
                        Kenya Industrial Property Institute. The requirements for the
                        registration of a trademark can be <a href="https://www.kipi.go.ke/index.php/trademarks"
                            target="_blank">here</a>.
