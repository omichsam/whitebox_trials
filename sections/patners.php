<script>
    document.addEventListener('DOMContentLoaded', function () {
        // Tab functionality
        const tabButtons = document.querySelectorAll('.tab-btn');
        const tabContents = document.querySelectorAll('.tab-content');

        tabButtons.forEach(button => {
            button.addEventListener('click', () => {
                const tabId = button.getAttribute('data-tab');

                // Update active tab button
                tabButtons.forEach(btn => btn.classList.remove('active'));
                button.classList.add('active');

                // Show active tab content
                tabContents.forEach(content => content.classList.remove('active'));
                document.getElementById(tabId).classList.add('active');
            });
        });

        // Initialize carousels
        initCarousel('capacity');
        initCarousel('technical');
        initCarousel('facility');

        function initCarousel(type) {
            const track = document.getElementById(`${type}-track`);
            const slides = document.querySelectorAll(`#${type}-track .partner-slide`);
            const prevBtn = document.querySelector(`.${type}-prev`);
            const nextBtn = document.querySelector(`.${type}-next`);

            let currentIndex = 0;
            let slidesPerView = calculateSlidesPerView();

            function calculateSlidesPerView() {
                if (window.innerWidth >= 1024) {
                    return 5; // lg screens
                } else if (window.innerWidth >= 768) {
                    return 3; // md screens
                } else {
                    return 2; // sm screens
                }
            }

            function updateCarousel() {
                const slideWidth = (100 / slidesPerView);
                track.style.transform = `translateX(-${currentIndex * slideWidth}%)`;
            }

            function goToSlide(index) {
                const maxIndex = Math.ceil(slides.length / slidesPerView) - 1;

                if (index < 0) {
                    currentIndex = maxIndex;
                } else if (index > maxIndex) {
                    currentIndex = 0;
                } else {
                    currentIndex = index;
                }

                updateCarousel();
            }

            function nextSlide() {
                goToSlide(currentIndex + 1);
            }

            function prevSlide() {
                goToSlide(currentIndex - 1);
            }

            // Event listeners
            nextBtn.addEventListener('click', nextSlide);
            prevBtn.addEventListener('click', prevSlide);

            // Auto-play (optional)
            let autoplay = setInterval(nextSlide, 4000);

            // Pause autoplay on hover
            const carousel = track.closest('.partner-carousel');
            carousel.addEventListener('mouseenter', () => {
                clearInterval(autoplay);
            });

            carousel.addEventListener('mouseleave', () => {
                autoplay = setInterval(nextSlide, 4000);
            });

            // Handle window resize
            window.addEventListener('resize', () => {
                slidesPerView = calculateSlidesPerView();
                updateCarousel();
            });

            // Initialize carousel
            updateCarousel();
        }
    });
</script>